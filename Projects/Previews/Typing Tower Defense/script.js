var level = 1;

const gameArea = document.body;
const inputBox = document.getElementById("tbox");
let enemies = [];

function spawnEnemy() {
  const word = words[Math.floor(Math.random() * words.length)];

  const container = document.createElement("div");
  container.className = "enemy-container";

  // Random horizontal position within viewport width minus 50 (enemy width)
  container.style.left = Math.random() * (window.innerWidth - 50) + "px";
  // Start below the screen
  container.style.top = window.innerHeight + "px";

  // Word above enemy
  const wordElem = document.createElement("div");
  wordElem.className = "enemy-word";
  wordElem.textContent = word;

  // Enemy image (ensure enemy.png exists!)
  const enemyImg = document.createElement("img");
  enemyImg.src = "enemy.png";
  enemyImg.alt = "enemy";

  container.appendChild(wordElem);
  container.appendChild(enemyImg);
  gameArea.appendChild(container);

  enemies.push({ word, element: container, yPos: window.innerHeight });

  moveEnemy(enemies[enemies.length - 1]);
}

function moveEnemy(enemyObj) {
  const speed = 3;

  function step() {
    enemyObj.yPos -= speed;
    enemyObj.element.style.top = enemyObj.yPos + "px";

    if (enemyObj.yPos + 50 < 0) {
      enemyObj.element.remove();
      enemies = enemies.filter(e => e !== enemyObj);
    } else {
      requestAnimationFrame(step);
    }
  }

  requestAnimationFrame(step);
}

inputBox.addEventListener("keydown", e => {
  if (e.key === "Enter") {
    const typed = inputBox.value.trim().toLowerCase();
    inputBox.value = "";
    for (let i = 0; i < enemies.length; i++) {
      if (enemies[i].word === typed) {
        enemies[i].element.remove();
        enemies.splice(i, 1);
        break;
      }
    }
  }
});

// Spawn enemies every 2 seconds
setInterval(spawnEnemy, 1000);

window.onload = () => {
  inputBox.focus();
  spawnEnemy(); // spawn one immediately on load
};
